package my.awesome.Garaz;

public class custom24model {
    int Image;
    String Text;


    public custom24model(int image, String text) {

        Image = image;
        Text = text;

    }



    public int getImage() {
        return Image;
    }

    public void setImage(int image) {
        Image = image;
    }

    public String getText() {
        return Text;
    }

    public void setText(String text) {
        Text = text;
    }


}
